function getLang() {
  const code = (getSettings().language || 'en').toLowerCase();
  return T[code] ? code : 'en';
}

function t(key, vars) {
  const lang = getLang();
  let s = (T[lang] && T[lang][key]) || (T.en && T.en[key]) || key;
  if (vars && typeof s === 'string') {
    Object.keys(vars).forEach(function (k) {
      s = s.split('{' + k + '}').join(String(vars[k]));
    });
  }
  return s;
}

function localeForApp() {
  const map = {
    en: 'en-IN',
    hi: 'hi-IN',
    pa: 'pa-IN',
    mr: 'mr-IN',
    bn: 'bn-IN',
    ta: 'ta-IN',
    te: 'te-IN',
  };
  return map[getLang()] || 'en-IN';
}

function applyI18n() {
  document.documentElement.lang = getLang() === 'en' ? 'en' : getLang();

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.setAttribute('placeholder', t(key));
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
    const key = el.getAttribute('data-i18n-aria');
    if (key) el.setAttribute('aria-label', t(key));
  });

  const page = document.body && document.body.getAttribute('data-page');
  const docKeys = {
    calc: 'docTitleCalc',
    cal: 'docTitleCal',
    hist: 'docTitleHist',
    tds: 'docTitleTds',
    settings: 'docTitleSettings',
  };
  if (page && docKeys[page]) document.title = t(docKeys[page]);
}

function fillLanguageSelect(selectEl) {
  if (!selectEl) return;
  selectEl.innerHTML = '';
  I18N_LANGS.forEach(function (o) {
    const opt = document.createElement('option');
    opt.value = o.code;
    opt.textContent = o.label;
    selectEl.appendChild(opt);
  });
  selectEl.value = getLang();
}
